int	ft_iterative_factorial(int nb)
{
	int j;
	int f;

	if (nb < 0 || nb > 12)
		return (0);
	if (nb >= 2)
	{
		j = 1;
		f = nb;
		while (j < nb)
		{
			f = f * (nb - j);
			j++;
		}
		return (f);
	}
	else
		return (1);
}
