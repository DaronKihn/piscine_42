#include "ft.h"

int		error_map(char *c, char *symb, int m, int k_str)
{
	int	i;
	int k;

	i = 0;
	k = 0;
	if ((c[0] == '\n') || (c[0] == '\0'))
		return (1);
	while (c[i] != '\0')
	{
		if (!(c[i] == symb[0] || c[i] == symb[1]
		|| c[i] == symb[2]) && ((i + 1) % m != 0))
			return (1);
		if (((i + 1) % m == 0) && (c[i] != '\n'))
			return (1);
		if (((i + 1) % m == 0))
			k++;
		i++;
	}
	if (k != k_str)
		return (1);
	return (0);
}
