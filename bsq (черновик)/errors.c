#include "ft.h"

int		error_map(char *c, char  empty, char  stop, char fill, int m)
{
	int		i;

	i = 0;
	if ((c[0] == '\n') || (c[0] == '\0'))
		return (1);
	while ( c[i] != '\0')
	{
		if (!(c[i] == empty || c[i] == stop || c[i] == fill) && ((i + 1) % m != 0))
			return (1);
		if (((i + 1) % m == 0) && (c[i] != '\n'))
			return (1);
		i++;
	}
	return (0);
}
